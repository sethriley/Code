def calculatefield(TERM):
    if TERM == "30-YRS":
        return "30 Years"
    if TERM == "20-YRS":
        return "20 Years"
    if TERM == "5-YRS":
        return "5 Years"
    if TERM == "50-YRS":
        return "50 Years"
    if TERM == "25-YRS":
        return "25 Years"
    if TERM == "10-YRS":
        return "10 Years"
    if TERM == "29-YRS & 4-MONTHS":
        return "29 Years & 4 Months"
    else:
        return "MARK"
